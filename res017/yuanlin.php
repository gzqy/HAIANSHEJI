      <h2>山  · 水 ·  地 · 形</h2>
      <p>
        地形是构成园林的骨架，地形要素的利用和改造，将影响到园林设计的形式、建筑的布局、
        植物配置、景观效果等。水体也是地形组成中一个不可缺少的部分，
        水是园林的灵魂，水体可以简单地划分为静水和动水两种类型。
        静水包括湖、池、塘等形式；动水主要包括河、溪、喷泉等形式；
        另外，水声、倒影也是园林水景的重要组成部分。


      </p>
    </div>
    <div class="laotitogg clsplay">
      <h2>植物</h2>
      <p>
        物是园林中有生命的构成要素。植物要素包括乔木、灌木、攀援植物、花卉、草坪等。
        植物的四季景观、本身形态、色彩、气味等都是园林造景的题材。
        园林植物与地形、水体、建筑、山石等的有机搭配，可以形成优美的环境。
　　      自然界往往是动物、植物共生共荣构成的生物生态景观，
        所以园林中除了考虑植物要素外，在条件允许的情况下，
        动物景观的规划，如游鱼、小鸟等可以为园林景观增色。

      </p>
